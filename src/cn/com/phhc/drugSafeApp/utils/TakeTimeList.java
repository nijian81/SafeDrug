package cn.com.phhc.drugSafeApp.utils;

import java.io.Serializable;

/**
 * Created by lenovo on 2015/4/1.
 */
public class TakeTimeList implements Serializable{
    String date;
    String time;

    public TakeTimeList(String date, String time) {
        this.date = date;
        this.time = time;
    }

    public TakeTimeList() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}

package cn.com.phhc.drugSafeApp.utils;

//用于药品查询界面列表项展示 尼见 2015-03-09
public class CompanyItem {
	String drugName;
    String name;
    String desc;
    String basicDrugId;
    String productDrugId;
    int flag; //用来标志是否处于选中状态，“1”为选中状态，“0”为非选中状态

    public CompanyItem(String desc) {
        this.desc = desc;
    }
    
    public CompanyItem(String name, String desc) {
        this.name = name;
        this.desc = desc;
    }
    
    public CompanyItem(String desc, String basicDrugId, String productDrugId) {
        this.desc = desc;
        this.basicDrugId = basicDrugId;
        this.productDrugId = productDrugId;
    }

	
    public CompanyItem(String name, String desc, String basicDrugId, String productDrugId) {
        this.name = name;
        this.desc = desc;
        this.basicDrugId = basicDrugId;
        this.productDrugId = productDrugId;
    }

    public CompanyItem(String drugName, String name, String desc,
			String basicDrugId, String productDrugId,int flag) {
		super();
		this.drugName = drugName;
		this.name = name;
		this.desc = desc;
		this.basicDrugId = basicDrugId;
		this.productDrugId = productDrugId;
		this.flag = flag;
	}

	public String getName() {
        return name;
    }

    public String getDesc() {
        return desc;
    }
    
    public String getDrugName() {
		return drugName;
	}

	public void setDrugName(String drugName) {
		this.drugName = drugName;
	}
	
	public String getProductDrugId() {
        return productDrugId;
    }

	public String getBasicDrugId() {
		return basicDrugId;
	}

	public void setBasicDrugId(String basicDrugId) {
		this.basicDrugId = basicDrugId;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	
}
